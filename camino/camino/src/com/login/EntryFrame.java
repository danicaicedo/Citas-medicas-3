    private void configurePanel(javax.swing.JPanel panel, javax.swing.JLabel label, String text) {
    panel.setBackground(new java.awt.Color(102, 153, 255));
    panel.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
    label.setText(text);
    panel.add(label);

    // AÃ±adir MouseListener para detectar clics en el panel
    
    
    panel.addMouseListener(new java.awt.event.MouseAdapter() {
        
        @Override
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            switch (text) {
                case "Agendar":
                     AgendarFrame agendarFrame = new AgendarFrame();
    agendarFrame.setVisible(true);
                    break;
                case "Calendario":
                     EntryFrame entryFrame = new EntryFrame();
    entryFrame.setVisible(true);
                    break;
                case "Historial":
                    
                    break;
            }
        }
    });
}

